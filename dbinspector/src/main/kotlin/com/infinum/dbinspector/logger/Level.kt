package com.infinum.dbinspector.logger

/**
 * Level of a log message.
 */
public enum class Level {
    /**
     * Debug log level.
     */
    DEBUG,

    /**
     * Info log level.
     */
    INFO,

    /**
     * Error log level.
     */
    ERROR,

    /**
     * No log level.
     */
    NONE
}
